n = input('Type something: ')
print('The primitive type of this value is:', type(n))
print('Is it only space?', n.isspace())
print('Is it a numeric?', n.isnumeric())
print('Is it alphabetic?', n.isalpha())
print('Is it a alphanumeric?', n.isalnum())
print('Is it only uppercase?', n.isupper())
print('Is it only lowercase?', n.islower())
print('Is it capitalized?', n.istitle())

